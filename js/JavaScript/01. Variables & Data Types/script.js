/*

1. We use 'var' keyword to decalre a variable in javaScript

2. There are 5 primitive(Non-Objects) data types in Js : 
    a) Number :   Float, integers
    b) String :   Sequence of characters or texts
    c) Boolean:   True/False
    d) Undefined: Data type of a variable which doesn't have a value yet
    e) Null:      Non-existent data type

*/


// outputing number data type on the browser console

var age = 20;
console.log(age); // outputs: 20

// outputing boolean data type on the browser console

var isTeacher = true;
console.log(isTeacher); // outputs: true

// outputing null data type on the browser console

var department;
console.log(department); // outputs: undefined

// outputing string on the browser console

var msg = "Hello World";
console.log(msg); // outputs: Hello World
