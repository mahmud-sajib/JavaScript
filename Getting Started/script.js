/*

The primitive(not object) data types of javaScript are:

1. string (can contain 'single' or "double" quotes)
2. number (consists of integer & floating point numbers)
3. boolean (can contain true or false values)
4. undefined (only contains the variable name doesn't contain a value)
5. null (non-existent)
6. NaN (Not A Number)

*/

// outputing string data type on the browser console

var name = "John Doe";
console.log(name);

// outputing number data type on the browser console

var age = 20;
console.log(age);

// outputing boolean data type on the browser console

var isTeacher = true;
console.log(isTeacher);

// outputing null data type on the browser console

var department;
console.log(department);
